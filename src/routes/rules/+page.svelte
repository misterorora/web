  <h2> Allgemeine Volleyballregeln </h2>
  <p>
    <b>Spielverlauf und Punkte:</b> Ein Volleyballspiel wird normalerweise in Sätzen gespielt, wobei das erste Team, das 25 Punkte erreicht (mit einem Vorsprung von mindestens zwei Punkten), den Satz gewinnt. Üblicherweise werden drei Gewinnsätze gespielt, aber in manchen Wettbewerben kann die Anzahl variieren. Im entscheidenden Satz wird oft bis 15 Punkte gespielt. Ein Punkt wird erzielt, wenn der Ball auf den Boden der gegnerischen Seite fällt, das andere Team einen Fehler begeht oder einen Regelverstoß begeht.
  </p>

  <p>
  <b> Mannschaften: </b> Jede Mannschaft besteht aus sechs Spielern, die auf dem Feld stehen. Es gibt verschiedene Positionen wie Zuspieler, Außenangreifer, Mittelblocker, Diagonalangreifer und Libero. Jede Position hat spezifische Aufgaben und Einschränkungen, die das Spielsystem beeinflussen.
</p>

<p>
<b> Aufschlag:</b> Das Spiel beginnt mit einem Aufschlag. Ein Spieler steht hinter der Grundlinie seines Teams und schlägt den Ball über das Netz auf die gegnerische Seite. Der Aufschlag muss von unten erfolgen und innerhalb des zugewiesenen Bereichs der Grundlinie erfolgen. Ein Aufschlagfehler führt zu einem Punkt für das gegnerische Team.
</p>

<p>
	<b> Berührung des Balls:</b> Jedes Team hat drei Berührungen, um den Ball über das Netz zu spielen. Die Berührungen können Aufschlag, Annahme, Zuspiel, Angriff oder Block sein. Ein Spieler darf den Ball nicht zweimal hintereinander berühren, es sei denn, die Berührungen erfolgen während einer einzigen Aktion wie dem Blocken oder dem ersten Kontakt nach dem gegnerischen Angriff.
</p>

<p>
	<b> Netzberührung und Netzspiel: </b> Spieler dürfen das Netz nicht berühren oder über das Netz treten, während sie den Ball spielen. Berührt ein Spieler das Netz während des Spiels, wird ein Punkt für das gegnerische Team vergeben. Spieler dürfen auch nicht über das Netz greifen, um den Ball zu blockieren oder zu spielen, es sei denn, der Ball kreuzt die Netzlinie und ist somit "auf dem Weg hinunter".
</p>
<p>
	<b> Blocken: </b> Beim Blocken versuchen Spieler, den Angriffsschlag des Gegners zu stoppen, indem sie sich in die Flugbahn des Balls bewegen und ihre Hände über das Netz strecken. Ein Block zählt nicht als Berührung, und ein Spieler kann direkt nach dem Blocken den Ball erneut berühren.
</p>
<p>
	<b> Seitenwechsel: </b> Die Mannschaften wechseln nach jedem Satz die Seiten des Spielfelds. Dies dient dazu, eventuelle Ungleichheiten aufgrund von Lichtverhältnissen oder anderen äußeren Bedingungen auszugleichen.
</p>
<p>
	<b> Aus- und Einwechslungen: </b> Spieler können während des Spiels ausgetauscht werden, aber es gelten spezifische Regeln, wann und wie dies geschehen kann. Der Libero ist ein spezieller Spieler, der unbegrenzt Ein- und Auswechslungen mit einem Spieler seiner Mannschaft durchführen kann, solange er sich in der Hinterfeldzone befindet.
</p>
<p>
	<b> Ball im Aus: </b> Wenn der Ball das Spielfeld verlässt, ohne dass ein Spieler des gegnerischen Teams ihn berührt, wird ein Punkt für das andere Team vergeben. Der Ball gilt als im Aus, wenn er außerhalb der Antennen den Boden berührt, die Decke oder einen Gegenstand über dem Spielfeld berührt oder einen der Pfosten oder die Netzanlage berührt.
</p>
<p>
	<b> Foulspiel und Strafen: </b> Es gibt verschiedene Arten von Fouls und Strafen im Volleyball, darunter Doppelschläge, Fußfehler, Berührung des Netzes, Behinderung, Aufschlagfehler usw. Diese können zu Punkten für das gegnerische Team führen oder den Verlust des Aufschlags bedeuten.
</p>
